# Turtle - wprowadzenie do interfejsu uzytkownika
import turtle
# Creating turtle screen 
t = turtle.Turtle()
# moving turtle 
t.forward(100)
t.right(90)
t.forward(50)
t.right(90)
t.backward(100)
# shorthend version
t.lt(45) # left(45)
t.fd(50) # forward(50)
t.rt(90) # right(90)
t.bk(100) # backward(100)
turtle.mainloop() # wait for user to do somethink

# Turtle to modul dostepny w Pythonie na podstawie jezyka Logo, ktory został stworzozny dla dzzieci aby uatrakcyjnic im nauke programowania. Turtle pozwala tworzyc proste kształty, figury, obrazki oraz animacje w oknie aplikacji dzieki czemu jest przyjemnym wstepem do programowania graficznego interfejsu uzytkownika



